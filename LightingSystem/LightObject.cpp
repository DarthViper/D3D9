/////////////////////////////////////////////////////////////////////////////////////////
void	LightObject::setLightType( int type )
{
	switch(type)
	{
	case DIRECTIONAL:
		m_eType = DIRECTIONAL;
		break;

	case OMNI:
		m_eType = OMNI;
		break;

	case SPOT:
		m_eType = SPOT;
		break;

	default:
		m_eType = DIRECTIONAL;
		break;
	}
}

